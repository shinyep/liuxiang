    def handle(self, *args, **options):
        # Using io.StringIO to treat the string as a file
        data_io = io.StringIO(self.customer_data_string)
        
        created_count = 0
        skipped_count = 0
        header_skipped = False

        for line in data_io:
            if not header_skipped:
                header_skipped = True
                continue  # Skip the header_skipped row

            line = line.strip()
            if not line:
                continue # Skip empty lines

            parts = line.split('\t')
            if len(parts) == 3:
                area_code = parts[0].strip()
                name = parts[1].strip()
                shipping_address = parts[2].strip()

                if not area_code: # Skip if area_code is empty
                    self.stdout.write(self.style.WARNING(f"Skipping line due to empty area code: {line}"))
                    skipped_count += 1
                    continue

                try:
                    customer, created = Customer.objects.get_or_create(
                        area_code=area_code,
                        defaults={'name': name, 'shipping_address': shipping_address}
                    )
                    if created:
                        self.stdout.write(self.style.SUCCESS(f'Successfully created customer: {area_code} - {name}'))
                        created_count += 1
                    else:
                        # Optionally update existing customer, or just report it was found
                        # For now, we just report if it was found and not created
                        # If you want to update, you would do:
                        # customer.name = name
                        # customer.shipping_address = shipping_address
                        # customer.save()
                        # self.stdout.write(self.style.SUCCESS(f'Customer {area_code} already exists. Updated.'))
                        self.stdout.write(self.style.NOTICE(f'Customer {area_code} - {name} already exists. Skipping creation.'))
                        skipped_count +=1 # Counting as skipped if it already exists and we are not updating.
                except Exception as e:
                    self.stdout.write(self.style.ERROR(f'Error processing line: {line}. Error: {e}'))
                    skipped_count += 1
            else:
                self.stdout.write(self.style.WARNING(f'Skipping malformed line (expected 3 parts): {line}'))
                skipped_count += 1
        
        data_io.close()
        self.stdout.write(self.style.SUCCESS(f'Import complete. {created_count} customers created, {skipped_count} customers skipped (already exist or error).'))
